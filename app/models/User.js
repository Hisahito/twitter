// creando una clase User para Twitter

class twitter {
    constructor(id, user, name, bio, dateCreated, lastUpdated) {
        this.id = id;
        this.user = user;
        this.name = name;
        this.bio = bio;
        this.dateCreated = dateCreated;
        this.lastUpdated = lastUpdated;
    }


}

module.exports = twitter;